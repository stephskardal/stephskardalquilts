jQuery(document).ready(function() {
  var optionsHtml = '';
  jQuery.each(rileyColors.colors, function(name, hex) {
    optionsHtml += '<a href="#" class="swatch" data-value="' + name + '" title="' + name + '"><span style="background-color:' + hex + ';"></span></a>';
    rileyColors.reverseLookup[hex] = name;
  });
  jQuery('div#swatches').html(optionsHtml);
  jQuery('.swatch:first').addClass('selected');
  jQuery('div.swatching').on('click', '.swatch', function(e) {
    e.preventDefault();
    jQuery('.swatch.selected').removeClass('selected');
    jQuery(this).addClass('selected');
  });
});
